#include "ft_utils.h"

void	ft_rev_int_tab(int *tab, size_t size)
{
	size_t	i;

	i = 0;
	while (i < size / 2)
	{
		ft_swap(&tab[i], &tab[i + 1]);
		i++;
	}
}
